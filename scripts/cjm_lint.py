TRUST_MARKER_RE = re.compile("[\U0001F7E2\U0001F7E1\U0001F534]")  # 🟢 🟡 🔴


def find_headings(lines: list[str]) -> list[tuple[int, int, str]]:
    """(line_index_0based, level, heading_text) для строк-заголовков markdown."""
    out = []
    for i, line in enumerate(lines):
        m = HEADING_RE.match(line)
        if m:
            out.append((i, len(m.group(1)), m.group(2).strip()))
    return out


def check_section_markers(lines: list[str]) -> list[Violation]:
    """Каждый H2-раздел (до следующего заголовка уровня <=2) должен содержать 🟢/🟡/🔴."""
    headings = find_headings(lines)
    violations = []
    n = len(lines)
    for idx, (line_i, level, text) in enumerate(headings):
        if level != 2:
            continue
        end = n
        for line_j, level_j, _ in headings[idx + 1 :]:
            if level_j <= 2:
                end = line_j
                break
        block = "\n".join(lines[line_i:end])
        if not TRUST_MARKER_RE.search(block):
            violations.append(
                Violation(
                    rule=3,
                    line=line_i + 1,
                    message=f"Раздел «{text}» не содержит маркера карты доверия (🟢/🟡/🔴).",
                    excerpt=lines[line_i].strip(),
                )
            )
    return violations


LEGEND_WORD_RE = re.compile("легенда", re.IGNORECASE)
# "Режим прогона: 🟡 РАЗВЕДОЧНЫЙ" / "🟢 ВАЛИДИРОВАННЫЙ" (report_template.md v1.3,
# MODE_BADGE) — см. докстринг модуля, раздел "[F2] правило 3 не применяется...".
_MODE_BADGE_LINE_RE = re.compile(r"режим\s+прогона", re.IGNORECASE)


def looks_like_trust_map_report(lines: list[str]) -> bool:
    """
    True, если документ (или, для lint_files(), объединённый набор документов)
    хоть где-то заявляет о намерении нести карту доверия 🟢/🟡/🔴 —
    слово «легенда» ИЛИ маркер 🟢/🔴 вне строки режим-бейджа паспорта
    методологии (см. `_MODE_BADGE_LINE_RE` и докстринг модуля за обоснованием
    исключения именно этой строки). `lines` — уже прошедшие `preprocess_lines`
    (несъёмные блоки дисклеймеров и HTML-комментарии замаскированы), как и во
    всех остальных проверках правила 3 ниже.
    """
    if any(LEGEND_WORD_RE.search(line) for line in lines):
        return True
    for line in lines:
        if _MODE_BADGE_LINE_RE.search(line):
            continue
        if "🟢" in line or "🔴" in line:
            return True
    return False


def check_legend_present(lines: list[str]) -> list[Violation]:
    violations = []
    text = "\n".join(lines)
    legend_line = None
    for i, line in enumerate(lines):
        if "легенда" in line.lower():
            legend_line = i + 1
            break
    if legend_line is None:
        violations.append(
            Violation(
                rule=3,
                line=0,
                message="Легенда карты доверия не найдена (нет строки со словом «Легенда»).",
            )
        )
    missing_markers = [m for m in ("🟢", "🟡", "🔴") if m not in text]
    if missing_markers:
        violations.append(
            Violation(
                rule=3,
                line=legend_line or 0,
                message=f"В документе не хватает маркеров карты доверия: {', '.join(missing_markers)}.",
            )
        )
    return violations


