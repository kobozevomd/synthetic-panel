# Любое дефисное составное слово (кириллица-дефис-кириллица, возможно несколько
# дефисов) длиннее AI_ISM_HYPHEN_MIN_LEN символов целиком — эвристика канцелярита
# без морфологического разбора (см. докстринг модуля за обоснованием порога).
HYPHENATED_WORD_RE = re.compile(r"\b[а-яёА-ЯЁ]+(?:-[а-яёА-ЯЁ]+)+\b")


def check_ai_isms(lines: list[str]) -> list[StyleWarning]:
    """
    Построчная проверка (НЕ логические блоки, как в правилах 1/2/4 — здесь нет
    риска "отмывания" word-wrap'ом: паттерны match'ат конкретное слово/фразу
    целиком, не требуют тега на той же логической единице). Дедупликация по
    позиции символов в строке — СКВОЗНАЯ по обоим проходам (см. докстринг модуля,
    "v1.3, задание [B3] §1.6 + §2.4", п.(б)): именные паттерны AI_ISM_PATTERNS
    (внутренне тоже дедуплицируются друг с другом) → дефисная длина
    HYPHENATED_WORD_RE. Второй проход пропускает спан, уже пойманный первым (см.
    `_overlaps` ниже) — например, «ориентированные» внутри «Ингредиент-
    ориентированные» не получает второго предупреждения от проверки длины
    дефисного слова.
    """
    warnings: list[StyleWarning] = []
    for i, line in enumerate(lines):
        matched_spans: list[tuple[int, int]] = []

        def _overlaps(span: tuple[int, int]) -> bool:
            return any(span[0] < e and s < span[1] for s, e in matched_spans)

        for label, pattern in AI_ISM_PATTERNS:
            for m in pattern.finditer(line):
                span = m.span()
                if _overlaps(span):
                    continue
                matched_spans.append(span)
                warnings.append(StyleWarning(line=i + 1, message=f"Возможный ИИ-изм: {label}.", excerpt=m.group(0)))

        for m in HYPHENATED_WORD_RE.finditer(line):
            token = m.group(0)
            if len(token) <= AI_ISM_HYPHEN_MIN_LEN:
                continue
            span = m.span()
            if _overlaps(span):
                continue  # уже отмечено более ранним проходом выше — не дублируем
            matched_spans.append(span)
            warnings.append(
                StyleWarning(
                    line=i + 1,
                    message=(
                        f"Длинное дефисное составное слово (>{AI_ISM_HYPHEN_MIN_LEN} символов) — "
                        f"похоже на канцелярит/ИИ-изм (spec v1.2 §Модуль 1 п.1)."
                    ),
                    excerpt=token,
                )
            )
    return warnings


