# [F1, ревью v1.4, находка №2, docs/review_v1.4.md §2.2, перефразировка 9, "минорное
# наблюдение"] Простой regex не отличает кириллические буквы от ВИЗУАЛЬНО похожих
# латинских ("омоглифы") — например латинскую "o" от кириллической "о". Точечная,
# НЕ универсальная нормализация (небольшой курируемый набор самых частых
# кириллизуемых латинских букв) — применяется ТОЛЬКО к копии текста для сопоставления
# паттернов ЭТОЙ красной зоны (см. check_visual_noticeability_red_zone ниже), НЕ
# глобально ко всему линтеру: тег источника [BA] и другие правила (например,
# английские вставки вроде "eye-tracking") не должны быть искажены заменой.
_CYRILLIC_HOMOGLYPHS_FROM_LATIN = str.maketrans(
    {
        "a": "а", "A": "А", "e": "е", "E": "Е", "o": "о", "O": "О", "p": "р", "P": "Р",
        "c": "с", "C": "С", "x": "х", "X": "Х", "y": "у", "Y": "У", "T": "Т", "H": "Н",
        "M": "М", "K": "К", "B": "В",
    }
)


def _normalize_shelf_homoglyphs(text: str) -> str:
    """См. комментарий к _CYRILLIC_HOMOGLYPHS_FROM_LATIN. Используется ТОЛЬКО для
    матчинга VISUAL_NOTICEABILITY_STOP_PATTERNS — has_tag (SOURCE_TAGS) и excerpt,
    показываемый пользователю, остаются на ОРИГИНАЛЬНОМ, не нормализованном тексте
    (см. check_visual_noticeability_red_zone), чтобы не исказить теги источника и
    не показать пользователю "исправленный" текст вместо реального."""
    return text.translate(_CYRILLIC_HOMOGLYPHS_FROM_LATIN)


def check_visual_noticeability_red_zone(blocks: list[tuple[int, str]]) -> list[Violation]:
    """
    Красная зона визуальных стимулов (spec_synthetic-panel_v1.4.md §1.4). Как и
    check_competitive_red_zone — освобождает от нарушения ТОЛЬКО тег источника
    (SOURCE_TAGS), НЕ слово «оценка»/«гипотеза» (см. докстринг модуля за
    обоснованием: полочная заметность структурно вне метода, пометка «гипотеза»
    эту структурную проблему не чинит).
    """
    violations = []
    for start_line, text in blocks:
        has_tag = any(tag in text for tag in SOURCE_TAGS)
        if has_tag:
            continue
        # Матчинг паттернов — на homoglyph-нормализованной копии (см. докстринг
        # _normalize_shelf_homoglyphs); has_tag выше и excerpt ниже — на ОРИГИНАЛЕ.
        normalized = _normalize_shelf_homoglyphs(text)
        for label, pattern in VISUAL_NOTICEABILITY_STOP_PATTERNS:
            if pattern.search(normalized):
                violations.append(
                    Violation(
                        rule=4,
                        line=start_line,
                        message=(
                            f"Красная зона визуального теста (spec_synthetic-panel_v1.4.md §1.4): "
                            f"{label} без тега источника ([BA]/[Mediascope]/[DSM]/[Росстат]/[опрос]/"
                            f"[клиент]). Полочная заметность и предсказание внимания — территория "
                            f"eye-tracking/attention-моделей, не смоделированных профилей; панель "
                            f"сравнивает считываемость смыслов, не физиологию внимания."
                        ),
                        excerpt=text,
                    )
                )
    return violations


