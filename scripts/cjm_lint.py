def mask_reference_blocks(lines: list[str]) -> list[str]:
    """
    Заменяет строки между (и включая) маркерами DISCLAIMER_BLOCK_MARKERS на пустые
    строки — сохраняя длину списка и номера строк ВНЕ маскируемых блоков. См.
    docstring модуля, пункт «Несъёмный блок дисклеймеров исключён из всех 4
    правил». Незакрытый маркер START без END маскирует до конца документа
    (лучше перебдеть и не проверить дисклеймер строгими правилами, чем поймать
    ложное нарушение в заведомо вычитанном несъёмном тексте).
    """
    out = list(lines)
    masking = False
    for i, line in enumerate(out):
        stripped = line.strip()
        if not masking and any(stripped == start for start, _ in DISCLAIMER_BLOCK_MARKERS):
            masking = True
            out[i] = ""
            continue
        if masking and any(stripped == end for _, end in DISCLAIMER_BLOCK_MARKERS):
            masking = False
            out[i] = ""
            continue
        if masking:
            out[i] = ""
    return out


HTML_COMMENT_OPEN = "<!--"
HTML_COMMENT_CLOSE = "-->"


def mask_html_comments(lines: list[str]) -> list[str]:
    """
    Вырезает содержимое HTML-комментариев (<!-- ... -->, включая многострочные),
    сохраняя количество и номера строк. Комментарии — служебные инструкции
    сборщику отчёта (шаблоны, пометки «копировать дословно» и т.п.): читатель
    собранного отчёта их не видит, поэтому правила 1/2/4 и warn-слой к ним не
    применяются. РЕШЕНО [review v1.2, находка №1 CRITICAL]: служебные
    комментарии references/competitive_report_template.md, перечисляющие
    «долю рынка»/«switch rate» как ЗАПРЕЩЁННЫЕ формулировки, ложно ловились
    конкурентной красной зоной правила 4.

    Порядок обязателен: вызывается ПОСЛЕ mask_reference_blocks — маркеры
    несъёмных блоков сами являются HTML-комментариями, и если вырезать
    комментарии первыми, маскировка дисклеймеров сломается.
    """
    out: list[str] = []
    in_comment = False
    for line in lines:
        visible: list[str] = []
        rest = line
        while rest:
            if in_comment:
                close = rest.find(HTML_COMMENT_CLOSE)
                if close == -1:
                    rest = ""
                else:
                    in_comment = False
                    rest = rest[close + len(HTML_COMMENT_CLOSE):]
            else:
                opened = rest.find(HTML_COMMENT_OPEN)
                if opened == -1:
                    visible.append(rest)
                    rest = ""
                else:
                    visible.append(rest[:opened])
                    in_comment = True
                    rest = rest[opened + len(HTML_COMMENT_OPEN):]
        out.append("".join(visible).rstrip())
    return out


def preprocess_lines(text: str) -> list[str]:
    """Единый препроцессор всех точек входа: маскировка несъёмных блоков → вырезание HTML-комментариев (порядок важен, см. докстринги обеих функций)."""
    return mask_html_comments(mask_reference_blocks(text.splitlines()))


