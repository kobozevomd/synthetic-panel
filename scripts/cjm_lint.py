RESPONDENT_WORD_RE = re.compile(r"\bреспондент\w*", re.IGNORECASE)


def check_respondent_terminology(lines: list[str]) -> list[StyleWarning]:
    """
    Построчная проверка слова «респондент» (и словоформ) — spec_synthetic-panel_v1.3.md
    §1.6 (Д6). Канон клиентского слоя: «N смоделированных профилей × M независимых
    генераций» вместо «N респондентов», «смоделированная реакция» вместо «цитата
    синтетического респондента» (см. references/report_template.md, SKILL.md).

    НЕ Violation, а StyleWarning: спецификация прямо оговаривает, что тексты о
    МЕТОДЕ с реальными респондентами (AAPOR, человеческий бенчмарк, тест-ретест
    повторяемости реальных людей) легально используют это слово — простой регэксп
    не отличает такой легальный текст от забытой старой терминологии, поэтому
    предупреждение, не блокирующее нарушение.
    """
    warnings: list[StyleWarning] = []
    for i, line in enumerate(lines):
        for m in RESPONDENT_WORD_RE.finditer(line):
            warnings.append(
                StyleWarning(
                    line=i + 1,
                    message=(
                        "Слово «респондент» в клиентском слое (spec v1.3 §1.6) — канон: «N "
                        "смоделированных профилей × M независимых генераций». Не нарушение, если "
                        "это текст о МЕТОДЕ с реальными респондентами (AAPOR, human-бенчмарк, "
                        "тест-ретест) — предупреждение только напоминает свериться."
                    ),
                    excerpt=m.group(0),
                )
            )
    return warnings


def collect_style_warnings(text: str) -> list[StyleWarning]:
    """
    Точка входа для одного текста (используется юнит-тестами, симметрично lint_text()).
    v1.3: объединяет ИИ-измы (check_ai_isms) и терминологию «респондент»
    (check_respondent_terminology) — оба прогоняются ПОСЛЕ preprocess_lines
    (маскировка несъёмных блоков дисклеймеров + HTML-комментариев), см. докстринги
    обеих функций.
    """
    lines = preprocess_lines(text)
    return check_ai_isms(lines) + check_respondent_terminology(lines)


def lint_files_style_warnings(paths: list[Path]) -> list[tuple[str, StyleWarning]]:
    """
    Многофайловая оркестрация warn-слоя — ОТДЕЛЬНАЯ от lint_files() функция и
    ОТДЕЛЬНЫЙ тип результата (см. докстринг модуля: контракт lint_files() с
    Violation.rule не меняется, чтобы не задеть 45 существующих тестов).
    """
    results: list[tuple[str, StyleWarning]] = []
    for p in paths:
        text = p.read_text(encoding="utf-8")
        for w in collect_style_warnings(text):
            results.append((p.name, w))
    return results


